alter table public.patients
add column created_by_active_doctor_id integer references public.active_doctors (active_doctor_id) on delete restrict,
add column account_status text not null default 'invited',
add column invited_at timestamptz not null default timezone('utc', now()),
add column activated_at timestamptz null;

update public.patients
set created_by_active_doctor_id = (
  select pd.active_doctor_id
  from public.patient_doctors as pd
  where pd.patient_id = public.patients.patient_id
  order by pd.is_primary desc, pd.created_at asc
  limit 1
)
where exists (
  select 1
  from public.patient_doctors as pd
  where pd.patient_id = public.patients.patient_id
)
  and public.patients.created_by_active_doctor_id is null;

alter table public.patients
