before update on public.approved_doctors
for each row
execute function public.set_updated_at();

create trigger set_active_doctors_updated_at
before update on public.active_doctors
for each row
execute function public.set_updated_at();

create trigger set_patients_updated_at
before update on public.patients
for each row
execute function public.set_updated_at();

create trigger set_patient_doctors_updated_at
before update on public.patient_doctors
for each row
execute function public.set_updated_at();

create trigger set_pharmacies_updated_at
before update on public.pharmacies
for each row
execute function public.set_updated_at();
